    private void btnSAVEMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSAVEMouseClicked
        // TODO add your handling code here:
        masuk.dbConnection();

        String nis = txtNIS.getText();
        String nama = txtNAMA.getText();
        String kelas = (String)cbKELAS.getSelectedItem();
        String jurusan = (String)cbJURUSAN.getSelectedItem();
        String tempat_lahir = txtTMPT.getText();
        String tampilan = "yyyy-MM-dd";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
        String tgl_lahir = String.valueOf(fm.format(DATE.getDate()));
        String alamat = txtALT.getText();
        String email = txtEMAIL.getText();
        String no_HP = txtHP.getText();
        String jenis_kelamin = (String)cbJK.getSelectedItem();

        try {
            java.sql.Statement stat = masuk.getConnection().createStatement();
            ResultSet data = stat.executeQuery("SELECT * FROM siswa WHERE nis ='" + txtNIS.getText() +"'");
            if(data.next()){
                JOptionPane.showMessageDialog(null, "NIS Sudah Ada", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                txtNIS.requestFocus();
            }else{
                String sql = "INSERT INTO siswa VALUES('" + nis + "'" + ",'" + nama + "'" +" ,'" + kelas + "'" + ",'" + jurusan + "'" + ",'"+ tempat_lahir +"'"+ " ,'"+ tgl_lahir +"'" + ",'"+ alamat +"'" + ",'"+ email +"'" + ",'"+ no_HP +"'"+",'"+ jenis_kelamin +"')";
                stat.executeUpdate(sql);

                bersih();
                JOptionPane.showMessageDialog(null, "Data Berhasil Di Simpan", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
            }

            stat.close();
        } catch (HeadlessException | SQLException e) {
        }
    }//GEN-LAST:event_btnSAVEMouseClicked

